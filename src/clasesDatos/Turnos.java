
package clasesDatos;


public class Turnos {
    private String _fechaTurno;
    private String _horaTurno;
    private String _dominioVeh;

    public Turnos() {
    }

    public String getFechaTurno() {
        return _fechaTurno;
    }

    public void setFechaTurno(String _fechaTurno) {
        this._fechaTurno = _fechaTurno;
    }

    public String getHoraTurno() {
        return _horaTurno;
    }

    public void setHoraTurno(String _horaTurno) {
        this._horaTurno = _horaTurno;
    }
    
    public String getDominioVeh() {
        return _dominioVeh;
    }

    public void setDominioVeh(String _dominioVeh) {
        this._dominioVeh = _dominioVeh;
    }
    
    
    
}
